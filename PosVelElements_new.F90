
!==================================================================================================
!    Вычисление орбитальных элементов (M0, w, Om, i, e, q, A_PAR, n) по положению и скорости...
!==================================================================================================

subroutine PosVelElements_new(ETIME, XV, AMASS, EPS, PERIT, Q, E, P, W, OM, I, M, N, A, Q2)

!==================================================================================================
!   Implicit none is your friend!
!==================================================================================================

implicit none

!==================================================================================================
!   Main parameters.
!==================================================================================================

real*8, intent(in), dimension(:) :: XV(6)
real*8, intent(in) :: ETIME

real*8, intent(out) :: PERIT, Q, E, P, W, OM, I, M, N, A, Q2

real*8, dimension(:) :: XV_ECL(6), PCH(3), PCH_W(2)
real*8 :: AMASS, EPS, CEP, R_PAR, V_PAR, A_PAR, RV, KP, P_PAR, EXANOM, V0, U0, GH
real(8) :: KAUSS

real*8, parameter :: KAUSS0 = 0.01720209895d0
real*8, parameter :: PI = 3.141592653589793d0
real*8, parameter :: RAD = 1.d0 !   180.d0/pi  (1.q0 чтобы выводить все угловые переменные в радианах. Если RAD = 180/pi, то будет принимать в градусах

real*8, parameter :: NEAR_PARAB = 1.d-10
real*8, parameter :: NEAR_PARAB_MIN = 1.d-14

!==================================================================================================
!   Находим большую полуось A_PAR...
!==================================================================================================
KAUSS = KAUSS0
CEP = KAUSS * dsqrt(1.d0 + AMASS)
KAUSS = KAUSS * dsqrt(1.d0 + AMASS)
R_PAR = dsqrt(XV(1)**2 + XV(2)**2 + XV(3)**2)
V_PAR = dsqrt(XV(4)**2 + XV(5)**2 + XV(6)**2)
RV = XV(1) * XV(4) + XV(2)* XV(5) + XV(3)* XV(6)
A_PAR = R_PAR * CEP**2 / (2.d0 * CEP**2 - R_PAR * V_PAR**2)

!--------------------------------------------------------------------------------------------------
!   Переходим в эклиптическую с/к...
!--------------------------------------------------------------------------------------------------

XV_ECL(1) = XV(1)
XV_ECL(2) = XV(2) * dcos(EPS) + XV(3) * dsin(EPS)
XV_ECL(3) = XV(3) * dcos(EPS) - XV(2) * dsin(EPS)
XV_ECL(4) = XV(4)
XV_ECL(5) = XV(5) * dcos(EPS) + XV(6) * dsin(EPS)
XV_ECL(6) = XV(6) * dcos(EPS) - XV(5) * dsin(EPS)

PCH(1) = XV_ECL(2) * XV_ECL(6) - XV_ECL(3) * XV_ECL(5)
PCH(2) = XV_ECL(1) * XV_ECL(6) - XV_ECL(3) * XV_ECL(4)
PCH(3) = XV_ECL(1) * XV_ECL(5) - XV_ECL(2) * XV_ECL(4)

KP = dsqrt(PCH(1)**2 + PCH(2)**2 + PCH(3)**2)
P_PAR = (KP / KAUSS)**2

!--------------------------------------------------------------------------------------------------
!   Если эллипс или гипербола...
!--------------------------------------------------------------------------------------------------

if(dabs(2.d0 * CEP**2 - R_PAR * V_PAR**2) > 1.d-14) then

    A = A_PAR

!--------------------------------------------------------------------------------------------------
!   находим наклон i...
!--------------------------------------------------------------------------------------------------

    I = dacos(PCH(3) / KP)
    if (I > PI*2.d0 .or. I < 0.d0) call Red2PI(I, I)

!--------------------------------------------------------------------------------------------------
!   находим узел Om...
!--------------------------------------------------------------------------------------------------

    if(PCH(2) /= 0.d0) then

        OM = datan(PCH(1) / PCH(2))
        call AtanSig_new(OM, PCH(1), PCH(2))

    else

        if(PCH(1) /= 0.d0) then
            if(PCH(1) > 0.d0) OM = PI / 2.d0
            if(PCH(1) < 0.d0) OM = PI / 2.d0 + PI
        else
            OM = 0.d0
        endif

    endif

!--------------------------------------------------------------------------------------------------
!   находим истинную аномалию v0 эксцентриситет e...
!--------------------------------------------------------------------------------------------------

    !Проверка
!    E = dsqrt((A - P_PAR) / A)

    V0 = datan((KP / KAUSS**2 * RV) / (P_PAR - R_PAR))

    call AtanSig_new(V0, KP / KAUSS**2 * RV, P_PAR - R_PAR)

    E = (P_PAR - R_PAR) / (R_PAR * dcos(V0))

!--------------------------------------------------------------------------------------------------
!   находим перигелийное расстояние q...
!--------------------------------------------------------------------------------------------------

    Q = A * (1.d0 - E)

!--------------------------------------------------------------------------------------------------
!   находим аргумент перигелия w...
!--------------------------------------------------------------------------------------------------

    PCH_W(1) = XV_ECL(3) * (1.d0 / dsin(I))
    PCH_W(2) = XV_ECL(1) * dcos(OM) + XV_ECL(2) * dsin(OM)

    U0 = datan(PCH_W(1) / PCH_W(2))
    call AtanSig_new(U0, PCH_W(1), PCH_W(2))

    W = U0 - V0
    
    if (W > PI*2.d0 .or. W < 0.d0) call Red2PI(W, W)
    
!--------------------------------------------------------------------------------------------------
!   находим среднее движение n...
!--------------------------------------------------------------------------------------------------

    N = KAUSS / dsqrt(dabs(A)**3.d0)

!--------------------------------------------------------------------------------------------------
!   находим среднюю аномалию M и момент прохождения перигелия...
!--------------------------------------------------------------------------------------------------

    if(E < 1.d0) then   !Если эллипс

        EXANOM = datan(dsqrt((1.d0 - E) / (1.d0 + E)) * dtan(V0 / 2.d0)) * 2.d0
        M = EXANOM - E * dsin(EXANOM)
        if (M > PI*2.d0 .or. M < 0.d0) call Red2PI(M, M)

        P = (Q / (1.d0 - E)) ** 1.5d0
        Q2 = Q * (1 + E) / (1 - E)
              
        if(M > PI) then
            PERIT = ETIME - (M - PI * 2.d0) / N
        else
            PERIT = ETIME - M / N
        endif
              
    else    !Если гипербола

        EXANOM = datanh(dsqrt((E - 1.d0) / (E + 1.d0)) * dtan(V0 / 2.d0)) * 2.d0
        M = E * dsinh(EXANOM) - EXANOM

        P = 0.d0
        Q2 = 0.d0
        
        PERIT = ETIME - M / N

        N = -1.d0

    endif

    W = W * RAD
    OM = OM * RAD
    I = I * RAD
    M = M * RAD
    N = N * RAD

!==================================================================================================
!   Если парабола...
!==================================================================================================

else

    A = 0.d0
    N = -1.d0
    P = 0.d0
    Q2 = 0.d0

!--------------------------------------------------------------------------------------------------
!   находим наклон i...
!--------------------------------------------------------------------------------------------------

    I = dacos(PCH(3) / KP)
    if (I > PI*2.d0 .or. I < 0.d0) call Red2PI(I, I)

!--------------------------------------------------------------------------------------------------
!   находим узел Om...
!--------------------------------------------------------------------------------------------------

    if(PCH(2) /= 0.d0) then

        OM = datan(PCH(1) / PCH(2))
        call AtanSig_new(OM, PCH(1), PCH(2))

    else

        if(PCH(1) /= 0.d0) then
            if(PCH(1) > 0.d0) OM = PI / 2.d0
            if(PCH(1) < 0.d0) OM = PI / 2.d0 + PI
        else
            OM = 0.d0
        endif

    endif

!--------------------------------------------------------------------------------------------------
!   находим истинную аномалию v0 и эксцентриситет e...
!--------------------------------------------------------------------------------------------------

    V0 = datan(RV / (KAUSS * dsqrt(P_PAR))) * 2.d0
!    call Red2PI(V0, V0)
    E = 1.d0

!--------------------------------------------------------------------------------------------------
!   находим перигелийное расстояние q...
!--------------------------------------------------------------------------------------------------

    Q = P_PAR * 0.5d0

!--------------------------------------------------------------------------------------------------
!   находим аргумент перигелия w...
!--------------------------------------------------------------------------------------------------

    PCH_W(1) = XV_ECL(3) * (1.d0 / dsin(I))
    PCH_W(2) = XV_ECL(1) * dcos(OM) + XV_ECL(2) * dsin(OM)

    U0 = datan(PCH_W(1) / PCH_W(2))
    call AtanSig_new(U0, PCH_W(1), PCH_W(2))

    W = U0 - V0
    
    call Red2PI(W, W)

!--------------------------------------------------------------------------------------------------
!   находим среднюю аномалию M...
!--------------------------------------------------------------------------------------------------

    M = (dsqrt(2.d0) / KAUSS) * ((RV / (KAUSS * dsqrt(P_PAR))) + 1.d0 / 3.d0 * (RV / (KAUSS * dsqrt(P_PAR)))**3)

!--------------------------------------------------------------------------------------------------
!   находим момент прохождения перигелия T...
!--------------------------------------------------------------------------------------------------

    PERIT = ETIME - M / dsqrt(Q**3.d0)

    W = W * RAD
    OM = OM * RAD
    I = I * RAD
    M = M * RAD
    N = N * RAD

!--------------------------------------------------------------------------------------------------
!   КОНЕЦ...
!--------------------------------------------------------------------------------------------------

endif

return
end

!==================================================================================================
!   Определение четверти угла по sin и cos, вычисленного через atan...
!==================================================================================================

subroutine AtanSig_new(A_PAR, SIN_A, COS_A)

!==================================================================================================
!   Implicit none is your friend!
!==================================================================================================

implicit none

!==================================================================================================
!   Main parameters.
!==================================================================================================

real*8 :: A_PAR, SIN_A, COS_A
real*8, parameter :: PI = 4.d0*datan(1.d0)

!==================================================================================================
!   ...
!==================================================================================================

if(SIN_A >= 0.d0) then
    if(COS_A >= 0.d0) then
        return
    else
        A_PAR = A_PAR + PI
    endif
else
    if(COS_A >= 0.d0) then
        A_PAR = A_PAR + 2.d0 * PI
    else
        A_PAR = A_PAR + PI
    endif
endif

return
end


subroutine Red2PI(A, B)

!==================================================================================================
!   Implicit none is your friend!
!==================================================================================================

implicit none

!==================================================================================================
!   Main parameters.
!==================================================================================================

real*8 :: A, B
real*8, parameter :: PI2 = 6.283185307179586d0
      
!==================================================================================================
!   ...
!==================================================================================================

B = A - idint(A / PI2) * PI2
if(A < 0.d0) B = B + PI2

return
end